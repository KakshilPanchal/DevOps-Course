#//Logical Operator  --> used to combine multiple condition
'''
And Operator - both condition true then true else False
OR operator  - if one condition is true then true
'''

#//Example
age=19
voter_id=True

if age >=18 and voter_id: #//AND operator is used
    print('Eligible for Vote')
else:
    print('Not Eligible for Vote')

#/Example for OR condition
Age=19
Voter_id=False

if Age >=18 or Voter_id: #//OR operator is used
    print('Eligible for Vote')
else:
    print('Not Eligible for Vote')


