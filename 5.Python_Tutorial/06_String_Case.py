#UPPER
msg="hello, world my name is kakshil panchal"
print(msg.upper())

#Lower Case
print(msg.lower())

#title case
print(msg.title()) #each first letter will be Capital

#Split case
print(msg.split(','))  #split the letter with comma. make value as new value

#to print starting each word
print(msg[0:12])   #indexing to print value based on index number
#remember in index you need to count actual number with one plus becuase it minus that index number
print(msg[:12])

print(msg[-7:])