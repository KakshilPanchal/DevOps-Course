def sub(num1,num2):  #substraction
    return num1 - num2
def add(num1,num2):
    return num1 + num2
def div(num1,num2): #division
    return num1 / num2
def multiply(num1,num2): #multiplication
    return num1 * num2
def even_or_odd(num): #even or odd
    if num % 2 == 0:
        print(f'Number is Even')
    else:
        print(f'Number is Odd')

